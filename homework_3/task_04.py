first_number = 10
second_number = 6
if first_number > second_number:
    print(f"Substraction {first_number} and {second_number} is "
          f"{first_number - second_number}")
elif second_number > first_number:
    print(f"Addition {first_number} and {second_number} is "
          f"{first_number + second_number}")
else:
    print(f"Raising {first_number} to the power of {second_number} is "
          f"{first_number ** second_number}")
